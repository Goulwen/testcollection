package testcollection.testcomparator;

public class Ville {

	private String departement;
	private String nom;
	
	public Ville(String departement, String nom) {
		this.departement = departement;
		this.nom = nom;
	}

	public String getDepartement() {
		return departement;
	}
	
	public String getNom() {
		return nom;
	}
	
	
}
